/**
  * @brief This function handles EXTI line0 interrupt.
  */
void EXTI0_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI0_IRQn 0 */
	  if (__HAL_GPIO_EXTI_GET_IT(GPIO_PIN_0) != RESET)
	  {
	    __HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_0);

	    /* Get current time immediately for accurate timing */
	    uint32_t current_time = GetMicros();

	    /* Read pin level after edge */
	    uint8_t pin_level = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0);

	    if (!ir_receiving)
	    {
	      /* Start of new signal - only on falling edge (IR active) */
	      if (pin_level == 0)
	      {
	        ir_receiving = 1;
	        ir_start_time = current_time;
	        edge_count = 0;

	        /* Record first edge */
	        edges[0].time_us = 0;
	        edges[0].level = 0;
	        edge_count = 1;
	      }
	    }
	    else if (edge_count < MAX_EDGES)
	    {
	      /* Record edge with relative timestamp */
	      edges[edge_count].time_us = current_time - ir_start_time;
	      edges[edge_count].level = pin_level;
	      edge_count++;
	    }
	  }
  /* USER CODE END EXTI0_IRQn 0 */
  //HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_0);
  /* USER CODE BEGIN EXTI0_IRQn 1 */
//
  /* USER CODE END EXTI0_IRQn 1 */
}

